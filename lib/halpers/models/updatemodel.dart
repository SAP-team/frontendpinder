class UpdateModel {
  String email, password, first, last, image, number;
  UpdateModel(String email, String password, String first, String last,
      String image, String number) {
    this.email = email;
    this.password = password;
    this.first = first;
    this.last = last;
    this.image = image;
    this.number = number;
  }
}
